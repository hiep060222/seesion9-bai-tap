let computera = +prompt("Mời bạn nhập số a");
let computerb = +prompt("Mời bạn nhập số b");
let calculation = prompt("mời bạn nhập các phép tính (+,-,*,/)");

switch (calculation) {
    case "+":
        alert(`Ket qua a + b = ${+computera + computerb}`)
    case "-":
        alert(`Ket qua a - b = ${+computera - computerb}`)
    case "+":
        alert(`Ket qua a * b = ${+computera * computerb}`)
    case "+":
        alert(`Ket qua a / b = ${+computera / computerb}`)
}
